class pattern1
{
    public static void main(String...args)
    {
        for(int i = 0; i < 5; i++)   //Rows
        {
            for(int j = 0; j < 5; j++)  //Columns
            {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}






